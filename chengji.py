while True:
    a = int(input('请输入你的年龄'))
    if a >= 100:
        print ('幼年')
    elif a >= 80:
        print ('少年')
    elif a >= 60:
        print ('成人')
    elif a >= 40:
        print ('中年')
    elif a >= 20:
        print ('壮年')
    elif a >= 10:
        print ('老年')
    else:
        print ('晚年')
